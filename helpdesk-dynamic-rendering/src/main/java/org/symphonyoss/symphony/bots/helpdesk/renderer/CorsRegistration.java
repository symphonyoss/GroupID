package org.symphonyoss.symphony.bots.helpdesk.renderer;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.servlet.config.annotation.CorsRegistry;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurer;
import org.springframework.web.servlet.config.annotation.WebMvcConfigurerAdapter;

/**
 * Spring configuration class to register CORS mappings.
 * <p>
 * Created by robson on 10/18/17.
 */
@Configuration
public class CorsRegistration {

  private static final String ALLOWED_METHODS = "*";

  private static final String CORS_MAPPING = "/**";

  /**
   * Configure CORS for the web resources accessed from other domains
   */
  @Bean
  public WebMvcConfigurer corsConfigurer() {
    return new WebMvcConfigurerAdapter() {
      @Override
      public void addCorsMappings(CorsRegistry registry) {
        registry.addMapping(CORS_MAPPING).allowedMethods(ALLOWED_METHODS);
      }
    };
  }

}
